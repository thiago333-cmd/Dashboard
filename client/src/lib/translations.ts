export function useTranslation(language: string = 'English') {
  const t = (key: string): string => {
    const languageTranslations = translations[language as keyof typeof translations] || translations['English'];
    return (languageTranslations as any)[key] || key;
  };
  
  const formatDate = (date: Date): string => {
    const localeMap: { [key: string]: string } = {
      'English': 'en-US',
      'Portuguese BR': 'pt-BR',
      'Spanish': 'es-ES',
      'French': 'fr-FR',
      'German': 'de-DE'
    };
    
    const locale = localeMap[language] || 'en-US';
    return new Intl.DateTimeFormat(locale, {
      weekday: 'long',
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    }).format(date);
  };
  
  return { t, formatDate };
}